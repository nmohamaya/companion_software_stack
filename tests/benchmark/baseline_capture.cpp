ScenarioBaseline& BaselineCapture::add_scenario(const std::string& name) {
    auto it = scenarios_.find(name);
    if (it == scenarios_.end()) {
        order_.push_back(name);
        it                       = scenarios_.emplace(name, ScenarioBaseline{}).first;
        it->second.scenario_name = name;
    }
    return it->second;
}

void BaselineCapture::finalise(const std::string& scenario_name, float iou_threshold,
                               uint32_t num_classes) {
    finalise(scenario_name, iou_threshold, num_classes, {});
}

void BaselineCapture::finalise(const std::string& scenario_name, float iou_threshold,
                               uint32_t                               num_classes,
                               const std::map<uint32_t, std::string>& class_names) {
    auto it = scenarios_.find(scenario_name);
    if (it == scenarios_.end()) {
        return;
    }
    ScenarioBaseline& s = it->second;
    s.iou_threshold     = iou_threshold;
    s.num_classes       = num_classes;
    s.frame_count       = static_cast<uint32_t>(s.frames.size());

    // Detection metrics.
    const DetectionMetrics det = compute_detection_metrics(s.frames, iou_threshold, num_classes);
    s.total_tp                 = det.total_tp();
    s.total_fp                 = det.total_fp();
    s.total_fn                 = det.total_fn();
    s.micro_precision          = det.micro_precision();
    s.micro_recall             = det.micro_recall();
    s.mean_ap                  = det.mean_ap();

    // Per-class breakdown.
    s.per_class.clear();
    for (const auto& [cls_id, cm] : det.per_class) {
        PerClassBaseline pcb;
        pcb.class_id        = cls_id;
        pcb.tp              = cm.tp;
        pcb.fp              = cm.fp;
        pcb.fn              = cm.fn;
        pcb.precision       = cm.precision();
        pcb.recall          = cm.recall();
        pcb.f1              = cm.f1();
        pcb.detection_count = cm.tp + cm.fp;

        auto ap_it = det.per_class_ap.find(cls_id);
        if (ap_it != det.per_class_ap.end()) {
            pcb.ap = ap_it->second;
        }

        auto name_it = class_names.find(cls_id);
        if (name_it != class_names.end()) {
            pcb.class_name = name_it->second;
        }

        s.per_class.push_back(std::move(pcb));
    }

    // Tracking metrics.
    const TrackingMetrics trk = compute_tracking_metrics(s.frames, iou_threshold);
    s.mota                    = trk.mota();
    s.motp                    = trk.motp();
    s.id_switches             = trk.id_switches;
    s.fragmentations          = trk.fragmentations;
}

const ScenarioBaseline* BaselineCapture::scenario(const std::string& name) const {
    auto it = scenarios_.find(name);
    return it != scenarios_.end() ? &it->second : nullptr;
}

std::vector<std::string> BaselineCapture::scenario_names() const {
    return order_;
}

std::string BaselineCapture::to_json() const {
    nlohmann::ordered_json root;
    root["_comment"] =
        "Perception baseline — auto-generated by BaselineCapture. "
        "Regenerate with: benchmarks/capture_baseline.sh (see benchmarks/README.md).";
    root["version"] = 1;

    nlohmann::ordered_json scenarios = nlohmann::ordered_json::object();
    for (const auto& name : order_) {
        auto it = scenarios_.find(name);
        if (it == scenarios_.end()) {
            continue;
        }
        const ScenarioBaseline& s = it->second;

        nlohmann::ordered_json sj;
        sj["frame_count"]   = s.frame_count;
        sj["iou_threshold"] = s.iou_threshold;
        sj["num_classes"]   = s.num_classes;

        // Detection.
        nlohmann::ordered_json det;
        det["total_tp"]        = s.total_tp;
        det["total_fp"]        = s.total_fp;
        det["total_fn"]        = s.total_fn;
        det["micro_precision"] = s.micro_precision;
        det["micro_recall"]    = s.micro_recall;
        det["mean_ap"]         = s.mean_ap;
        sj["detection"]        = std::move(det);

        // Per-class.
        nlohmann::ordered_json pc = nlohmann::ordered_json::array();
        for (const auto& c : s.per_class) {
            nlohmann::ordered_json cj;
            cj["class_id"]        = c.class_id;
            cj["class_name"]      = c.class_name;
            cj["tp"]              = c.tp;
            cj["fp"]              = c.fp;
            cj["fn"]              = c.fn;
            cj["precision"]       = c.precision;
            cj["recall"]          = c.recall;
            cj["f1"]              = c.f1;
            cj["ap"]              = c.ap;
            cj["detection_count"] = c.detection_count;
            pc.push_back(std::move(cj));
        }
        sj["per_class"] = std::move(pc);

        // Tracking.
        nlohmann::ordered_json trk;
        trk["mota"]           = s.mota;
        trk["motp"]           = s.motp;
        trk["id_switches"]    = s.id_switches;
        trk["fragmentations"] = s.fragmentations;
        sj["tracking"]        = std::move(trk);

        // Latency — parse back from string if present.
        if (!s.latency_json.empty()) {
            auto parsed = nlohmann::json::parse(s.latency_json, nullptr, false);
            if (!parsed.is_discarded()) {
                sj["latency"] = std::move(parsed);
            }
        }

        scenarios[name] = std::move(sj);
    }
    root["scenarios"] = std::move(scenarios);

    return root.dump(2) + "\n";
}

bool BaselineCapture::write_json(const std::string& path) const {
    std::error_code ec;
    const auto      parent = std::filesystem::path(path).parent_path();
    if (!parent.empty()) {
        std::filesystem::create_directories(parent, ec);
        if (ec) {
            return false;
        }
    }
    std::ofstream out(path);
    if (!out) {
        return false;
    }
    out << to_json();
    return out.good();
}

bool BaselineCapture::load_json(const std::string& path) {
    std::ifstream in(path);
    if (!in) {
        return false;
    }

    nlohmann::json root;
    try {
        in >> root;
    } catch (const nlohmann::json::parse_error&) {
        return false;
    }

    if (!root.contains("scenarios") || !root["scenarios"].is_object()) {
        return false;
    }

    order_.clear();
    scenarios_.clear();

    for (auto it = root["scenarios"].begin(); it != root["scenarios"].end(); ++it) {
        const std::string& name = it.key();
        const auto&        sj   = it.value();

        order_.push_back(name);
        ScenarioBaseline& s = scenarios_[name];
        s.scenario_name     = name;
        s.frame_count       = sj.value("frame_count", 0U);
        s.iou_threshold     = sj.value("iou_threshold", 0.5F);
        s.num_classes       = sj.value("num_classes", 0U);

        if (sj.contains("detection")) {
            const auto& d     = sj["detection"];
            s.total_tp        = d.value("total_tp", 0U);
            s.total_fp        = d.value("total_fp", 0U);
            s.total_fn        = d.value("total_fn", 0U);
            s.micro_precision = d.value("micro_precision", 0.0);
            s.micro_recall    = d.value("micro_recall", 0.0);
            s.mean_ap         = d.value("mean_ap", 0.0);
        }

        if (sj.contains("per_class") && sj["per_class"].is_array()) {
            for (const auto& cj : sj["per_class"]) {
                PerClassBaseline pcb;
                pcb.class_id        = cj.value("class_id", 0U);
                pcb.class_name      = cj.value("class_name", std::string{});
                pcb.tp              = cj.value("tp", 0U);
                pcb.fp              = cj.value("fp", 0U);
                pcb.fn              = cj.value("fn", 0U);
                pcb.precision       = cj.value("precision", 0.0);
                pcb.recall          = cj.value("recall", 0.0);
                pcb.f1              = cj.value("f1", 0.0);
                pcb.ap              = cj.value("ap", 0.0);
                pcb.detection_count = cj.value("detection_count", 0U);
                s.per_class.push_back(std::move(pcb));
            }
        }

        if (sj.contains("tracking")) {
            const auto& t    = sj["tracking"];
            s.mota           = t.value("mota", 0.0);
            s.motp           = t.value("motp", 0.0);
            s.id_switches    = t.value("id_switches", 0U);
            s.fragmentations = t.value("fragmentations", 0U);
        }

        if (sj.contains("latency")) {
            s.latency_json = sj["latency"].dump();
        }
    }

    return true;
}

